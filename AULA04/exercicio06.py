#criar um programa em while para pedir ao usuario que insira número até ele digitar "sair"
#armazene os numeros em uma lista
#use o for para mostrar somente os numeros pares da lista

print("PROGRAMA PARA DIGITAR UM NÚMERO OU SAIR")
programa = input("Digite um número: ")
while programa != "Sair":
    print("Tente Novamente")
    programa = input("Digite um número: ")
print("Fim")


