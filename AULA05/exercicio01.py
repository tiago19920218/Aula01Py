#soma (+)
def soma (numero1, numero2 ):
    print(f"O resultado da soma é {numero1+numero2}")

soma(5, 5)

#subtração (-)
def subtracao (numero1, numero2 ):
    print(f"O resultado da subtracao é {numero1-numero2}")

subtracao(10, 5)

#multiplicação (*)
def multiplicacao (numero1, numero2 ):
    print(f"O resultado da multiplicao é {numero1*numero2}")

multiplicacao(5, 5)

#divisão (/)
def divisao (numero1, numero2 ):
    print(f"O resultado da divisao é {numero1//numero2}")

divisao(10, 5)